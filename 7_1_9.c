/*
 * 输入一个四位数，将其加密后输出，方法是将该数每一位上的数字+9，
 * 然后然后然后！！！1然后除以10取余
 * 最后将千位和十位上的数字呼唤，百位和个位数字互换
 *
 */


#include<stdio.h>
int main()
{
    int x,g,s,b,q;
    scanf("%d",&x);
    g = (x%10 +9)%10;
    s = (x%100/10 +9)%10;
    b = (x%1000/100 +9)%10;
    q = (x/1000 +9)%10;
    printf("%d%d%d%d",s,g,q,b);
    return 0;
}